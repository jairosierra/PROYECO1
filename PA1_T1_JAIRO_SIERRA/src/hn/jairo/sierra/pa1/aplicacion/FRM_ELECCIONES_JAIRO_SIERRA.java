    private void btnganadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnganadorActionPerformed
             if  (participante.getContador1()>participante.getContador2() && participante.getContador1()>participante.getContador3()){
           Icon imagen1 = new ImageIcon(getClass().getResource("/IMAGENES/game.jpg"));
       JOptionPane.showMessageDialog(null, "El ganador es: The Game of Thrones"+"\nCon "+participante.getContador1()+" votos.","Ganador de Elecciones",JOptionPane.INFORMATION_MESSAGE,imagen1);
       
       }else if(participante.getContador2()>participante.getContador1() && participante.getContador2()>participante.getContador3()){
           Icon imagen2= new ImageIcon(getClass().getResource("/IMAGENES/breaking.jpg"));
          JOptionPane.showMessageDialog(null, "El ganador es: Breaking Bad"+"\nCon "+participante.getContador2()+" votos.","Ganador de Elecciones",JOptionPane.INFORMATION_MESSAGE,imagen2);
       
       }else if(participante.getContador3()>participante.getContador1() && participante.getContador3()>participante.getContador2()){
           Icon imagen3= new ImageIcon(getClass().getResource("/IMAGENES/soprano.jpg"));
          JOptionPane.showMessageDialog(null, "El ganador es: The Sopranos"+"\nCon "+participante.getContador3()+" votos.","Ganador de Elecciones",JOptionPane.INFORMATION_MESSAGE,imagen3);
       }
            
    }//GEN-LAST:event_btnganadorActionPerformed

