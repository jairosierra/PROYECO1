    public void borrar(){
        txtgot.setText("");
        txtbbad.setText("");
        txttsop.setText("");
        txtpergot.setText("");
        txtperbbad.setText("");
        txtpertsop.setText("");
        txttotalvotos.setText("");
        participante.contador1=0;
        participante.contador2=0;
        participante.contador3=0;
        participante.porcentaje1=0;
        participante.porcentaje2=0;
        participante.porcentaje3=0;
        participante.totalvotoss=0;
    }    
    public void porcentaje(){
  participante.totalvotoss= participante.getContador1()+participante.getContador2()+participante.getContador3();
       txttotalvotos.setText(String.valueOf(participante.getTotalvotos()));
       //totalvotos =Integer.parseInt(txtTotalVotos.getText());
       //porcentaje1= Integer.parseInt(txtVotos1.getText());
       
       participante.porcentaje1 = participante.getContador1()*100/participante.getTotalvotos();
       txtpergot.setText(String.valueOf(participante.getPorcentaje1()+"%"));
        //porcentaje2= Integer.parseInt(txtVotos2.getText());
       participante.porcentaje2 = participante.getContador2()*100/participante.getTotalvotos();
       txtperbbad.setText(String.valueOf(participante.getPorcentaje2()+"%"));
        //porcentaje3= Integer.parseInt(txtVotos3.getText()); 
       participante.porcentaje3 = participante.getContador3()*100/participante.getTotalvotos();
       txtpertsop.setText(String.valueOf(participante.getPorcentaje3()+"%"));
  }
    
   
    
